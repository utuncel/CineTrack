package org.com.model.enums;

/**
 * An enumeration representing the different types of cinematics.
 *
 * <p>This enum is used to distinguish between the three main categories of content
 * in the application: anime, movies, and series.</p>
 *
 * <ul>
 *   <li>{@link #ANIME} - Represents anime content.</li>
 *   <li>{@link #MOVIE} - Represents movies.</li>
 *   <li>{@link #SERIES} - Represents TV or web series.</li>
 * </ul>
 *
 * @author umut
 * @version 1.0
 */
public enum Type {
  /**
   * Represents anime content.
   */
  ANIME,

  /**
   * Represents movies.
   */
  MOVIE,

  /**
   * Represents TV or web series.
   */
  SERIES
}
